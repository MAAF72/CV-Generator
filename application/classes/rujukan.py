class Rujukan:
    def __init__(self):
        self.nama = None
        self.instansi = None
        self.no_hp = None
        self.email = None

    def set_nama(self, nama):
        self.nama = nama

    def set_instansi(self, instansi):
        self.instansi = instansi
    
    def set_no_hp(self, no_hp):
        self.no_hp = no_hp

    def set_email(self, email):
        self.email = email
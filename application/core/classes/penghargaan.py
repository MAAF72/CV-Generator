class Penghargaan:
    def __init__(self):
        self.nama = None
        self.instansi = None
        self.tahun = None
        self.deskripsi = None

    def set_nama(self, nama):
        self.nama = nama

    def set_instansi(self, instansi):
        self.instansi = instansi

    def set_tahun(self, tahun):
        self.tahun = tahun
        
    def set_deskripsi(self, deskripsi):
        self.deskripsi = deskripsi
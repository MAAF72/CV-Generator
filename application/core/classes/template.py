class Template:
    def __init__(self):
        self.nama = None
        self.deskripsi = None
        self.file = None

    def set_nama(self, nama):
        self.nama = nama

    def set_deskripsi(self, deskripsi):
        self.deskripsi = deskripsi

    def set_file(self, file):
        self.file = file